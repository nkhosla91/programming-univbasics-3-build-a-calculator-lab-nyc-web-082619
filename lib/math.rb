def addition(num1, num2)
  add = num1 + num2
  return add
end

def subtraction(num1, num2)
  sub = num1 - num2
  return sub
end

def division(num1, num2)
  div = num1 / num2
  return div
end

def multiplication(num1, num2)
  mult = num1 * num2
  return mult
end

def modulo(num1, num2)
 mod = num1 % num2
 return mod
end

def square_root(num)
  sqr = Math.sqrt(num)
  return sqr
end
